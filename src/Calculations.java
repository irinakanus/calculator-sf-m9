public class Calculations {

    public static double addingNumbers (double num1, double num2){
        return (num1 + num2);
    }

    public static double subtractingNumbers (double num1, double num2){
        return (num1 - num2);
    }

    public static double multiplyingNumbers (double num1, double num2){
        return (num1 * num2);
    }

    public static double dividingNumbers (double num1, double num2){
        return (num1 / num2);
    }
}
